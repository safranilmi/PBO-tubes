    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        //hitung
        int hitung = 0;
        
        int temp = 0;
        try {
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/galeri_itera","root","");
            ResultSet rs = cn.createStatement().executeQuery("select * from produk");
            
            while(rs.next()){
                if(rs.getInt("id_produk")==Integer.parseInt(jTextField1.getText())){
                    hitung = rs.getInt(3); 
                }              
            }
            
            temp = hitung * Integer.parseInt(angka2.getText());
            sum = sum + temp;
            
            
        } catch (SQLException ex) {
            Logger.getLogger(bayar.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        totalbox.setText(""+sum);
        
        int kembalianjumlah = Integer.parseInt(bayarbox.getText()) - Integer.parseInt(totalbox.getText());
        kembalian.setText(""+kembalianjumlah);
    }//GEN-LAST:event_jButton2ActionPerformed

